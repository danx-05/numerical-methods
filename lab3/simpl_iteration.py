def solve(x_0, e):
    """
    x^2-3=0
    x = x - C(x^2-3)
    C = 0.1
    :param x_0: начальное приближение
    :param e: точность
    :return: конечное приближение
    """
    x_1 = x_0 - 0.1 * (x_0**2 - 3)
    k = 1
    while abs(x_1 - x_0) > e: # условие остановки алгоритма
        k += 1
        x_0 = x_1
        x_1 = x_0 - 0.1 * (x_0**2 - 3) # итерационная формула
    return x_1, k
